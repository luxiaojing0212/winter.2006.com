function ajax(param,node){
    //实例化xhr
    var xhr = new XMLHttpRequest();
    //监听
    xhr.onreadystatechange = function(){
        if(xhr.readyState == 4 && xhr.status == 200){
            var json_str = xhr.responseText
            var boj = JSON.parse(json_str);
            if(boj.erron>0)
            {
                alert(boj.msg)
                node.value = ""         //清空 input
                node.focus()            //获取焦点
            }
        }
    }
    //open
    xhr.open('GET','register-new1.php?account='+param);
    //发送
    xhr.send();
}

//账号
